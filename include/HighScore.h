#ifndef HIGHSCORE_H
#define HIGHSCORE_H
#include <iostream>
#include "Ltimer.h"
#include <QtCore/QString>
//-------------------------------------------------------------------------------------------------------------------------
/// @file HighScore.h
/// @author Eligijus Titas
/// @version 1.0
/// @class HighScore
/// @brief This class writes out and reads the highscore for the game
//--------------------------------------------------------------------------------------------------------------------------
class HighScore
{
  public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor for HighScore  class
    //----------------------------------------------------------------------------------------------------------------------
    HighScore();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief starts the highscore timer
    //----------------------------------------------------------------------------------------------------------------------
    void highScoreStart();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief method pauses the highscore
    //----------------------------------------------------------------------------------------------------------------------
    void highScorePause();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief reads the value from a txt file
    //----------------------------------------------------------------------------------------------------------------------
    void highScoreRead();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets the current score
    //----------------------------------------------------------------------------------------------------------------------
    void setCurrentScore();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief writes the highscore to a txt file
    //----------------------------------------------------------------------------------------------------------------------
    void highScoreWrite();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief sets the highscore
    //----------------------------------------------------------------------------------------------------------------------
    void setHighScore();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief returns the highscore
    //----------------------------------------------------------------------------------------------------------------------
    inline int getHighScore(){return m_highScore;}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief returns the current score
    //----------------------------------------------------------------------------------------------------------------------
    inline int getCurrentScore(){return m_currentScore;}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief returns the current score timer
    //----------------------------------------------------------------------------------------------------------------------
    int getScoreTime() {return m_scoreTimer.getTicks();}
  private:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief current score value
    //----------------------------------------------------------------------------------------------------------------------
    int m_currentScore;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief highscore value
    //----------------------------------------------------------------------------------------------------------------------
    int m_highScore;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief string value for the read file
    //----------------------------------------------------------------------------------------------------------------------
    QString m_highScoreString;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief timer for the score
    //----------------------------------------------------------------------------------------------------------------------
    LTimer m_scoreTimer;
};

#endif // HIGHSCORE_H
